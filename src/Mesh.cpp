void Mesh::initQuad() {
    float quadVertices[] = { // vertex attributes for a quad that fills the entire screen in Normalized Device Coordinates.
            // positions   // texCoords
            -1.0f,  1.0f,  0.0f, 1.0f,
            -1.0f, -1.0f,  0.0f, 0.0f,
             1.0f, -1.0f,  1.0f, 0.0f,
            -1.0f,  1.0f,  0.0f, 1.0f,
             1.0f, -1.0f,  1.0f, 0.0f,
             1.0f,  1.0f,  1.0f, 1.0f
    };
    vertices.clear();
    indices.clear();
    for (int i = 0; i < 6; i++) {
        int begin = i * 4;
        Vertex v;
        v.pos = WPos(quadVertices[begin], quadVertices[begin + 1], 0);
        v.norm = WPos(0, 0, 0);
        v.tex = SPos(quadVertices[begin + 2], quadVertices[begin + 3]);
        vertices.push_back(v);
        indices.push_back(i);
    }
}

